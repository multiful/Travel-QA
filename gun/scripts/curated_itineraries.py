def make_workbook() -> openpyxl.Workbook:
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "input"

    # 헤더
    header_fill = PatternFill("solid", fgColor="2F5496")
    header_font = Font(bold=True, color="FFFFFF", size=11)
    for col_idx, name in enumerate(COLS, 1):
        cell = ws.cell(row=1, column=col_idx, value=name)
        cell.fill = header_fill
        cell.font = header_font
        cell.alignment = Alignment(horizontal="center")

    # 데이터 채우기
    row_idx = 2
    n_plans = 0
    n_places = 0
    for plan_id, sido, sigungu, period, base_date, places in ITINERARIES:
        n_plans += 1
        source = SOURCE_MAP.get(plan_id, "공통지식")
        for day, order, name, hint in places:
            n_places += 1
            # 다일 일정은 day마다 일자 +1
            from datetime import datetime, timedelta
            date_obj = datetime.strptime(base_date, "%Y-%m-%d") + timedelta(days=day - 1)
            date_str = date_obj.strftime("%Y-%m-%d")
            row = [source, plan_id, sido, sigungu, period, date_str,
                   day, order, name, hint]
            for col_idx, val in enumerate(row, 1):
                ws.cell(row=row_idx, column=col_idx, value=val)
            row_idx += 1

    # 컬럼 폭
    widths = [22, 10, 14, 14, 12, 12, 6, 8, 22, 12]
    for col_idx, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(col_idx)].width = w

    # 데이터 검증 (드롭다운)
    dv_sido = DataValidation(type="list",
                             formula1=f'"{",".join(SIDO_OPTIONS)}"', allow_blank=True)
    dv_sido.add("C2:C2000")
    ws.add_data_validation(dv_sido)
    dv_period = DataValidation(type="list",
                               formula1=f'"{",".join(PERIOD_OPTIONS)}"', allow_blank=True)
    dv_period.add("E2:E2000")
    ws.add_data_validation(dv_period)
    dv_cat = DataValidation(type="list",
                            formula1=f'"{",".join(CATEGORY_HINTS)}"', allow_blank=True)
    dv_cat.add("J2:J2000")
    ws.add_data_validation(dv_cat)

    ws.freeze_panes = "A2"

    # ── guide 시트 ──
    ws_guide = wb.create_sheet("guide")
    notes = [
        "📋 큐레이션 일정 40개 (AI001~AI040) 사전 입력됨",
        "",
        f"  · 총 일정: {n_plans}개",
        f"  · 총 장소: {n_places}개",
        f"  · 평균 5~6 places/itinerary",
        f"  · 시도 분포: 서울(8) 부산(3) 제주(3) 경상도(8) 전라도(7) 강원(4) 충청·대전(4) 경기·인천(4)",
        "",
        "🔧 다음 작업",
        "  ① 이 input 시트에 추가 일정 채우기 (AI041부터)",
        "     - 트리플 (https://triple.guide)",
        "     - 마이리얼트립 (https://myrealtrip.com)",
        "     - 네이버 블로그 ([지역] 추천 코스 검색)",
        "  ② 100개까지 채운 후:",
        "     python3 gun/scripts/build_itinerary_excel.py",
        "  ③ gun/data/itinerary_results_YYYYMMDD.xlsx 결과 확인",
        "  ④ match_failed.csv 확인 → 이름 보정 → 재실행",
        "",
        "📊 컬럼 의미",
    ]
    for r, line in enumerate(notes, 1):
        ws_guide.cell(row=r, column=1, value=line)
    for r, name in enumerate(COLS, len(notes) + 2):
        ws_guide.cell(row=r, column=1, value=name).font = Font(bold=True)

    ws_guide.column_dimensions["A"].width = 60

    return wb


